#define fr(i,n)for(int i=0,i##e=n;i<i##e;++i)
#define ec(i,m)fr(i,m.size())
#define srt(v)sort(v.begin(),v.end())
using namespace std;

struct DNASingleMatcher {
	int longestMatch(string a, string b) {
		int mx = 0;
		if (a.size() < b.size()) swap(a, b);
		ec(i, a) {
			for (int j = i; j < ie; ++j) {
				string ss = a.substr(i, j - i + 1);
				if (b.find(ss) != string::npos)
					mx = max(mx, int(ss.size()));
			}
		}
		return mx;
	}
};


double test0() {
	string p0 = "AAAAAAAAAAAAAAAAAAAAACCCGGGGGGGGGGGGG";
	string p1 = "AAAACCCGGGGGGGGGGGGGGGGTTTTTTTTTGGGGGGGGGGGG";
	DNASingleMatcher * obj = new DNASingleMatcher();
	clock_t start = clock();
	int my_answer = obj->longestMatch(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p2 = 20;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p2 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
