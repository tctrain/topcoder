	Airways * obj = new Airways();
	clock_t start = clock();
	double my_answer = obj->distance(p0, p1, p2);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	double p3 = 437.99999999999994;
	cout <<"Desired answer: " <<endl;
