struct FoxProgression {
	int theCount(vector <int> s) {
		if (s.size() == 1) return -1;
		const int q = s.size();
		bool a = true, g = !(s[1] % s[0]), y = true;
		int av = s[1] - s[0], gv = s[1] / s[0];
		for (int i = 2; i < q; ++i) {
			if (s[i] - s[i-1] != av)
				a = false;
			if (s[i] / s[i-1] != gv || s[i] % s[i-1] != 0)
				g = false;
		}
		if (a && g && s.back() + av != s.back() * gv)
			return 2;
		else if (a || g) return 1;
		else return 0;
	}
};


