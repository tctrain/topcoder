struct ManhattanMovement {
	double getDistance(int a, int b, int x0, int y0) {
		if (a == 0) return fabs(1.0 / b - y0);
		if (b == 0) return fabs(1.0 / a - x0);
		return min(fabs(y0 - (1.0 - a * x0) / b), fabs(x0 - (1.0 - b * y0) / a));
	}
};


double test0() {
	int p0 = 1;
	int p1 = 2;
	int p2 = -2;
	int p3 = 3;
	ManhattanMovement * obj = new ManhattanMovement();
	clock_t start = clock();
	double my_answer = obj->getDistance(p0, p1, p2, p3);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	double p4 = 1.5;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p4 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p4 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
