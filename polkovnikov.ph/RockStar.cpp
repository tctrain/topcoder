struct RockStar {
	int getNumSongs(int ff, int fs, int sf, int ss) {
		if (fs > 0)
			return ff + 1 + ss + 2 * min(sf, fs - 1) + (fs - 1 < sf);
		if (ff > 0)
			return ff;
		return ss + !!sf;
	}
};


double test0() {
	int p0 = 100;
	int p1 = 0;
	int p2 = 0;
	int p3 = 200;
	RockStar * obj = new RockStar();
	clock_t start = clock();
	int my_answer = obj->getNumSongs(p0, p1, p2, p3);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p4 = 100;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p4 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p4 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
