struct CircleGame {
	int cardsLeft(string d) {
		int pos;
		while ((pos = d.find("K")) != string::npos)
			d.erase(d.begin() + pos);
		string s = "A23456789TJQK";
		bool f;
		do {
			f = false;
			for (int i = 0, ilen = d.size(); i < ilen; ++i) {
				int j = (i + 1) % ilen;
				if (s.find(d[i]) + s.find(d[j]) + 2 == 13) {
					d.erase(d.begin() + max(i, j));
					d.erase(d.begin() + min(i, j));
					f = true;
					break;
				}
			}
		} while (f);
		return d.size();
	}
};


double test0() {
	string p0 = "KKKKKKKKKK";
	CircleGame * obj = new CircleGame();
	clock_t start = clock();
	int my_answer = obj->cardsLeft(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p1 = 0;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p1 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
