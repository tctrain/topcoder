struct BadClock {
	int parse(string s) {
		stringstream ss;
		ss << s;
		int h, m, se;
		char c;
		ss >> h >> c >> m >> c >> se;
		se += (h * 60 + m) * 60;
		return se;
	}
	int gcd(int x, int y) {
		while (y) {
			int r = x % y;
			x = y;
			y = r;
		}
		return x;
	}
	double nextAgreement(string tt, string st, int hg) {
		int t1 = parse(tt);
		int t2 = parse(st);
		int d = t1 - t2;
		if (hg < 0) {
			if (d > 0)
				d -= 24 * 60 * 60;
		} else {
			if (d < 0)
				d += 24 * 60 * 60;
		}
		hg = gcd(d, hg);
		cout << d << endl;
		return double(d) / hg;
	}
};


