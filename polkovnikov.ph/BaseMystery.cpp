struct BaseMystery {
	static int conv(string s, int b) {
		int r = 0;
		for (int i = 0, ilen = s.size(); i < ilen; ++i) {
			int c;
			if ('0' <= s[i] && s[i]<= '9') {
				c = s[i] - '0';
			} else {
				c = s[i] - 'A' + 10;
			}
			if (c >= b)
				throw 42;
			r *= b;
			r += c;
		}
		return r;
	}
	vector <int> getBase(string eq) {
		int p1 = eq.find("+");
		int p2 = eq.find("=");
		string a = eq.substr(0, p1);
		string b = eq.substr(p1 + 1, p2 - p1 - 1);
		string c = eq.substr(p2 + 1);
		vector<int> r;
		for (int i = 2; i <= 20; ++i) {
			try {
				if (conv(a, i) + conv(b, i) == conv(c, i))
					r.push_back(i);
			} catch (...) {}
		}
		return r;
	}
};


