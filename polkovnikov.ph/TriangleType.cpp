struct TriangleType {
	string type(int a, int b, int c) {
		int s[] = {a, b, c};
		sort(s, s+3);
		a = s[0], b = s[1], c = s[2];
		if (a + b <= c) return "IMPOSSIBLE";
		if (a * a + b * b == c * c) {
			return "RIGHT";
		} else if (a * a + b * b < c * c) {
			return "OBTUSE";
		} else {
			return "ACUTE";
		}
	}
};


double test0() {
	int p0 = 3;
	int p1 = 4;
	int p2 = 5;
	TriangleType * obj = new TriangleType();
	clock_t start = clock();
	string my_answer = obj->type(p0, p1, p2);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	string p3 = "RIGHT";
	cout <<"Desired answer: " <<endl;
	cout <<"\t\"" << p3 <<"\"" <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t\"" << my_answer<<"\"" <<endl;
	if (p3 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
