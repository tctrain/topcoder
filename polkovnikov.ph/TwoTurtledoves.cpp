struct TwoTurtledoves {
	int presentType(int n) {
		int d = 1, s = 0, m = 0;
		for (; m < n; ++d) {
			s += d;
			m += s;
			
		}
		for (int i = m - s + 1; i <= m; i += d, --d)
			if (i <= n && n < i + d)
				return d;
		return 0;
	}
};


double test0() {
	int p0 = 10;
	TwoTurtledoves * obj = new TwoTurtledoves();
	clock_t start = clock();
	int my_answer = obj->presentType(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p1 = 1;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p1 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
