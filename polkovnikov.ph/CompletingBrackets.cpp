#define fr(i,n)for(int i=0,i##e=n;i<i##e;++i)
#define ec(i,m)fr(i,m.size())
#define srt(v)sort(v.begin(),v.end())
using namespace std;

struct CompletingBrackets {
	string complete(string text) {
		int c = 0, d = 0;
		ec(i, text) {
			if (text[i] == '[') {
				++c;
			} else {
				if (c == 0) {
					++d;
					c = 0;
				} else --c;
			}
		}
		return string(d, '[') + text + string(c, ']');
	}
};


double test0() {
	string p0 = "[[";
	CompletingBrackets * obj = new CompletingBrackets();
	clock_t start = clock();
	string my_answer = obj->complete(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	string p1 = "[[]]";
	cout <<"Desired answer: " <<endl;
	cout <<"\t\"" << p1 <<"\"" <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t\"" << my_answer<<"\"" <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
