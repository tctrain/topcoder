struct LCMRange {
	int gcd(int x, int y) {
		while (y) {
			int r = x % y;
			x = y;
			y = r;
		}
		return x;
	}
	int lcm(int first, int last) {
		int lcm = 1;
		for (int i = first; i <= last; ++i)
			lcm = lcm * i / gcd(lcm, i);
		return lcm;
	}
};


double test0() {
	int p0 = 1;
	int p1 = 5;
	LCMRange * obj = new LCMRange();
	clock_t start = clock();
	int my_answer = obj->lcm(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p2 = 60;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p2 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
