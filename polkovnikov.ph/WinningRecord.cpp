struct WinningRecord {
	vector <int> getBestAndWorst(string g) {
		int mni = -1, mxi = -1;
		int mns = 0, mxs = 0;
		int w = 0;
		fori(i, g) {
			if (g[i] == 'W') ++w;
			if (i < 2) continue;
			if (mni == -1 || mns * (i + 1) >= w * mni) {
				mns = w;
				mni = i + 1;
			}
			if (mxi == -1 || mxs * (i + 1) <= w * mxi) {
				mxs = w;
				mxi = i + 1;
			}
		}
		vector<int> r(2);
		r[0] = mxi;
		r[1] = mni;
		return r;
	}
};


double test0() {
	string p0 = "WWWWWLLLLLLLLLLLLLLL";
	WinningRecord * obj = new WinningRecord();
	clock_t start = clock();
	vector <int> my_answer = obj->getBestAndWorst(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int t1[] = { 5,  20 };
	vector <int> p1(t1, t1+sizeof(t1)/sizeof(int));
	cout <<"Desired answer: " <<endl;
	cout <<"\t{ ";
	if (p1.size() > 0) {
		cout <<p1[0];
		for (int i=1; i<p1.size(); i++)
			cout <<", " <<p1[i];
		cout <<" }" <<endl;
	}
	else
		cout <<"}" <<endl;
	cout <<endl <<"Your answer: " <<endl;
	cout <<"\t{ ";
	if (my_answer.size() > 0) {
		cout <<my_answer[0];
		for (int i=1; i<my_answer.size(); i++)
			cout <<", " <<my_answer[i];
		cout <<" }" <<endl;
	}
	else
		cout <<"}" <<endl;
	if (my_answer != p1) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
