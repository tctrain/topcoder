#define all(a)a.begin(),a.end()
#define forn(i,n)for(int i=0,i##e=n;i<i##e;++i)
#define fori(i,m)forn(i,m.size())
#define each(i,m)for(typeof(m.begin())i=m.begin(),i##e=m.end();i!=i##e;++i)
#define srt(s)sort(all(s))
#define dump(m)copy(all(m),ostream_iterator<typeof(m[0])>(cout,"\n"))
using namespace std;

struct LevelUp {
	int toNextLevel(vector <int> en, int re) {
		int s = 0;
		fori(i, en) {
			en[i] -= s;
			s += en[i];
		}
		fori(i, en) {
			int d = min(en[i], re);
			re -= d;
			if (re == 0)
				return d == en[i] ? en[i+1] : en[i] - d;
		}
	}
};


double test0() {
	int t0[] = {150,450,900,1800};
	vector <int> p0(t0, t0+sizeof(t0)/sizeof(int));
	int p1 = 133;
	LevelUp * obj = new LevelUp();
	clock_t start = clock();
	int my_answer = obj->toNextLevel(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p2 = 17;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p2 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
