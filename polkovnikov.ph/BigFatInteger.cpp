struct BigFatInteger {
	int minOperations(int A, int B) {
		map<int, int> d;
		const int lim = int(sqrt(double(A)));
		for (int i = 2; i <= lim; ++i) {
			while (A % i == 0) {
				++d[i];
				A /= i;
			}
		}
		if (A > 1) ++d[A];
		int mxrc = 0;
		bool need = false;
		for (map<int, int>::iterator i = d.begin(); i != d.end(); ++i) {
			int r = i->second * B, rc = 0, bc = 0;
			while (r) {
				++rc;
				if (r % 2) ++bc;
				r /= 2;
			}
			mxrc = max(mxrc, rc);
			if (bc != 1) need = true;
		}
		
		return d.size() + (mxrc - 1) + need;
	}
};


double test0() {
	int p0 = 6;
	int p1 = 1;
	BigFatInteger * obj = new BigFatInteger();
	clock_t start = clock();
	int my_answer = obj->minOperations(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p2 = 2;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p2 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
