struct Apothecary {
	vector <int> balance(int W) {
		vector<int > r;
		int c = 1;
		while (W) {
			int d = W % 3;
			if (d == 1) {
				r.push_back(c);
			} else if (d == 2) {
				r.push_back(-c);
				W += 1;
			}
			W /= 3;
			c *= 3;
		}
		srt(r);
		return r;
	}
};


