struct EyeDrops {
	double closest(int sleepTime, int k) {
		int m = (24 - sleepTime) * 60;
		return k == 1 ? 24 * 60 : (m % (k - 1) == 0 ? double(m) /  k : double(m) / (k - 1));
	}
};


double test0() {
	int p0 = 8;
	int p1 = 2;
	EyeDrops * obj = new EyeDrops();
	clock_t start = clock();
	double my_answer = obj->closest(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	double p2 = 720.0;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p2 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
