struct InsideOut {
	string unscramble(string line) {
		reverse(line.begin(), line.begin() + line.size() / 2);
		reverse(line.begin() + line.size() / 2, line.end());
		return line;
	}
};


double test0() {
	string p0 = "I ENIL SIHTHSIREBBIG S";
	InsideOut * obj = new InsideOut();
	clock_t start = clock();
	string my_answer = obj->unscramble(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	string p1 = "THIS LINE IS GIBBERISH";
	cout <<"Desired answer: " <<endl;
	cout <<"\t\"" << p1 <<"\"" <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t\"" << my_answer<<"\"" <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
