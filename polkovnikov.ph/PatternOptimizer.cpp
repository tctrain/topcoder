#define fr(i,n)for(int i=0,i##e=n;i<i##e;++i)
#define ec(i,m)fr(i,m.size())
#define srt(v)sort(v.begin(),v.end())
using namespace std;

struct PatternOptimizer {
	string optimize(string p) {
		int stars = 0, quest = 0;
		string r;
		ec(i, p) {
			if (p[i] == '*') {
				++stars;
			} else if (p[i] == '?') {
				++quest;
			} else {
				if (stars != 0) r += '*';
				if (quest != 0) r += string(quest, '?');
				stars = 0;
				quest = 0;
				r += p[i];
			}
		}
		if (stars != 0) r += '*';
		if (quest != 0) r += string(quest, '?');
		stars = 0;
		quest = 0;
		return r;
	}
};


double test0() {
	string p0 = "*??*a";
	PatternOptimizer * obj = new PatternOptimizer();
	clock_t start = clock();
	string my_answer = obj->optimize(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	string p1 = "*??a";
	cout <<"Desired answer: " <<endl;
	cout <<"\t\"" << p1 <<"\"" <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t\"" << my_answer<<"\"" <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
