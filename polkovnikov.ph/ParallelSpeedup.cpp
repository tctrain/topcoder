struct ParallelSpeedup {
	int numProcessors(int k, int overhead) {
		long long t = numeric_limits<long long>::max();
		int mp = -1;
		for (long long p = 1; p <= k; ++p) {
			long long nt = overhead * p * (p - 1) / 2 + k / p + !!(k % p);
			if (nt < t) {
				t = nt;
				mp = p;
			}
		}
		return mp;
	}
};


double test0() {
	int p0 = 12;
	int p1 = 1;
	ParallelSpeedup * obj = new ParallelSpeedup();
	clock_t start = clock();
	int my_answer = obj->numProcessors(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p2 = 2;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p2 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
