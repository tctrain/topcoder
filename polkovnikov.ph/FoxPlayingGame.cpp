struct FoxPlayingGame {
	double theMax(int nA, int nB, int paramA, int paramB) {
		double mx = 0;
		mx = max(mx, nA * paramA / 1000.0);
		cout << mx << endl;
		if (nB % 2 == 1)
			mx = max(mx, nA * paramA / 1000.0 * pow(abs(paramB) / 1000.0, nB - 1));
		cout << mx << endl;
		if (paramB > 0)
			mx = max(mx, nA * paramA / 1000.0 * pow(paramB / 1000.0, nB));
		cout << mx << endl;
		return mx;
	}
};


double test0() {
	int p0 = 5;
	int p1 = 4;
	int p2 = 3000;
	int p3 = 2000;
	FoxPlayingGame * obj = new FoxPlayingGame();
	clock_t start = clock();
	double my_answer = obj->theMax(p0, p1, p2, p3);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	double p4 = 240.0;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p4 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p4 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
