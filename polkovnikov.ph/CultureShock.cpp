#define fr(i,n)for(int i=0,i##e=n;i<i##e;++i)
#define ec(i,m)fr(i,m.size())
#define srt(v)sort(v.begin(),v.end())
using namespace std;

struct CultureShock {
	string translate(string text) {
		string r;
		string w;
		ec(i, text) {
			if (text[i] != ' ') {
				w += text[i];
			} else {
				if (w == "ZEE") w = "ZED";
				r += w;
				w.clear();
				r += ' ';
			}
		}
		if (w == "ZEE") w = "ZED";
		r += w;
		return r;
	}
};


double test0() {
	string p0 = "THE TWENTY SIXTH LETTER OF THE ALPHABET IS ZEE";
	CultureShock * obj = new CultureShock();
	clock_t start = clock();
	string my_answer = obj->translate(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	string p1 = "THE TWENTY SIXTH LETTER OF THE ALPHABET IS ZED";
	cout <<"Desired answer: " <<endl;
	cout <<"\t\"" << p1 <<"\"" <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t\"" << my_answer<<"\"" <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
