struct Percents {
	int minSamples(string percent) {
		stringstream ss;
		ss << percent;
		int x, y; char c;
		ss >> x >> c >> y;
		x = 100 * x + y;
		for (int i = 1; i <= 10000; ++i) {
			for (int j = 0; j <= i; ++j) {
				int k = j * 100000 / i;
				k = k / 10 + (k % 10 >= 5);
				if (k == x)
					return i;
			}
		}
		return 10000;
	}
};


double test0() {
	string p0 = "25.00%";
	Percents * obj = new Percents();
	clock_t start = clock();
	int my_answer = obj->minSamples(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p1 = 4;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p1 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
