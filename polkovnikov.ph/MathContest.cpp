#include <string>
struct MathContest {
	int countBlack(std::string b,int R,int r=0) {
		for(int z=b.size(),i=z*R-1,f=0,v=0,c=0,d=i;i>=0;--i)((b[(f?d--:c++)%z]=='W')^v?f:(++r,v))^=1;
		return r;
	}
};

double test0() {
	string p0 = "BBWWB";
	int p1 = 1;
	MathContest * obj = new MathContest();
	clock_t start = clock();
	int my_answer = obj->countBlack(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p2 = 2;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p2 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
