#define all(a)a.begin(),a.end()
#define forn(i,n)for(int i=0,i##e=n;i<i##e;++i)
#define fori(i,m)forn(i,m.size())
#define each(i,m)for(typeof(m.begin())i=m.begin(),i##e=m.end();i!=i##e;++i)
#define srt(s)sort(all(s))
using namespace std;

struct Stairs {
	int designs(int maxHeight, int minWidth, int totalHeight, int totalWidth) {
		int d = 0;
		forn(i, maxHeight) {
			int h = i + 1;
			if (totalHeight % h != 0) continue;
			int c = totalHeight / h - 1;
			if (c == 0) continue;
			if (totalWidth % c != 0) continue;
			if (totalWidth / c < minWidth) continue;
			++d;
		}
		return d;
	}
};


double test0() {
	int p0 = 22;
	int p1 = 25;
	int p2 = 100;
	int p3 = 100;
	Stairs * obj = new Stairs();
	clock_t start = clock();
	int my_answer = obj->designs(p0, p1, p2, p3);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p4 = 1;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p4 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p4 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
