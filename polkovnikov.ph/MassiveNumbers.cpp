struct MassiveNumbers {
	string getLargest(string numberA, string numberB) {
		stringstream ss;
		ss << numberA << ' ' << numberB;
		int a, b, c, d;
		char x;
		ss >> a >> x >> b >> c >> x >> d;
		return b * log(a) > d * log(c) ? numberA : numberB;
	}
};


double test0() {
	string p0 = "3^100";
	string p1 = "2^150";
	MassiveNumbers * obj = new MassiveNumbers();
	clock_t start = clock();
	string my_answer = obj->getLargest(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	string p2 = "3^100";
	cout <<"Desired answer: " <<endl;
	cout <<"\t\"" << p2 <<"\"" <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t\"" << my_answer<<"\"" <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
