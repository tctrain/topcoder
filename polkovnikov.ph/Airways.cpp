struct Airways {
	double distance(int n, int E, int N) {
		const double pi = atan(1) * 4;
		double len = sqrt(E * E + N * N);
		double alpha = atan2(double(N), double(E));
		if (alpha < 0) alpha += 2 * pi;
		double beta = 2.0 * pi / n;
		double gamma = alpha - int(alpha / beta) * beta;
		return len * (sin(gamma) + sin(beta - gamma))
			/ sin(pi - beta);
	}
};


