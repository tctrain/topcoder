struct CalendarRecycle {
	bool leap(int x) {
		return (x % 4 == 0) ^ (x % 100 == 0) ^ (x % 400 == 0);
	}
	int wday(int y) {
		int d = 6;
		for (int yy = 2003; yy <= y; ++yy)
			d += 365 + leap(yy);
		d %= 7;
		return d;
	}
	int useAgain(int year) {
		for (int y = year+1; ; ++y) {
			if (leap(year) != leap(y)) continue;
			if (wday(y) != wday(year)) continue;
			return y;
		}
	}
};


