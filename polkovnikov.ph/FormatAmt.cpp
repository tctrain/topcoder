using namespace std;

struct sepr : public numpunct<char> {
    char do_thousands_sep() const  {
        return ',';
    }
    string do_grouping() const {
		return "\03";
	}
};

struct FormatAmt {
	string amount(int dollars, int cents) {
		stringstream ss;
		ss.imbue(locale(ss.getloc(), new sepr()));
		ss << '$' << dollars << '.' << setfill('0') << setw(2) << cents;
		string s;
		ss >> s;
		return s;
	}
};


double test0() {
	int p0 = 123456;
	int p1 = 0;
	FormatAmt * obj = new FormatAmt();
	clock_t start = clock();
	string my_answer = obj->amount(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	string p2 = "$123,456.00";
	cout <<"Desired answer: " <<endl;
	cout <<"\t\"" << p2 <<"\"" <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t\"" << my_answer<<"\"" <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
