struct RugSizes {
	int rugCount(int area) {
		int c = 0;
		for (int i = 1; i <= area; ++i) {
			int j = area / i;
			if (area % i != 0) continue;
			if (i > j) break;
			if (i != j && i % 2 == 0 && j % 2 == 0) continue;
			++c;
		}
		return c;
	}
};



