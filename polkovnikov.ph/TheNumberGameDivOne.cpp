struct TheNumberGameDivOne {
	string find(long long n) {
		if (n == 0) return "Brus";
		for (long long i = 2; i <= n; i *= 4)
			if (n == i)
				return "Brus";
		return n % 2 == 0 ? "John" : "Brus";
	}
};


double test0() {
	long long p0 = 6LL;
	TheNumberGameDivOne * obj = new TheNumberGameDivOne();
	clock_t start = clock();
	string my_answer = obj->find(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	string p1 = "John";
	cout <<"Desired answer: " <<endl;
	cout <<"\t\"" << p1 <<"\"" <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t\"" << my_answer<<"\"" <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
